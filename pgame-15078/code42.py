prodi="Informatika UMMU"

print(prodi)

#cetak kolom 0 (pertama) dari variabel prodi
print(prodi[0])

#cetak kolom 0 dengan keterangan
print("prodi [0] :", prodi[0])

#cetak kolom awal sampai sampai kolom ke 4
print(prodi[:3])

#cetak kolom awal sampai kolom ke 4 dengan keterangan
print("prodi [:3] :", prodi[:3])

#cetak semua kecuali kolom 0 (pertama)
print(prodi[1:])

#cetak semua kecuali kolom terakhir
print(prodi[:-1])

#cetak semua kecuali kolom terakhir dengan keterangan
print("prodi [:-2] : ", prodi[:-2])
