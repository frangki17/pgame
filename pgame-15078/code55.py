#Buat Variabel A dan B
A = 50
B = 20

#gunakan kondisi if
if A > B:
	print("A lebih besar dari B")

#gunakan if, elif
if A > B:
	print("A lebih besar dari B")
elif A == B:
	print("A sama dengan B")

#gunakan if, elif,else
if A > B:
	print("A lebih besar dari B")
elif A == B:
	print("A sama dengan B")
else:
	print("A Lebih Kecil dari B")
